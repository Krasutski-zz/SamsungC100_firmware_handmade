void font_printTime(char x,char y)
{
	RTC_Time_t time;

	GetTime(&time);

	font_printDec(x,y,time.hours&0x7f);
	font_printChar(x+2*6,y,':');
	font_printDec(x+3*6,y,time.min&0x7f);
	font_printChar(x+5*6,y,':');
	font_printDec(x+6*6,y,time.sec&0x7f);
}





