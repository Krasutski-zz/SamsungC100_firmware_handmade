volatile unsigned short colorFt=0x7e0,colorBg=0;


static void font_printChar(const u8 x,const u8 y,const u8 asscii)
{
	char ShiftBit=1;

	LCD_base=0x43;
	LCD_base=0x10+x;
	LCD_base=0x14+x;
	LCD_base=0x42;
	LCD_base=y;
	LCD_base=y+7;

	do
	{
		if( font5x8[asscii][0] & ShiftBit ){LCD_data=colorFt;}else{LCD_data=colorBg;}
		if( font5x8[asscii][1] & ShiftBit ){LCD_data=colorFt;}else{LCD_data=colorBg;}
		if( font5x8[asscii][2] & ShiftBit ){LCD_data=colorFt;}else{LCD_data=colorBg;}
		if( font5x8[asscii][3] & ShiftBit ){LCD_data=colorFt;}else{LCD_data=colorBg;}
		if( font5x8[asscii][4] & ShiftBit ){LCD_data=colorFt;}else{LCD_data=colorBg;}
		ShiftBit<<=1;
	}while(ShiftBit!=0);
}

void font_printlcd(unsigned char *text,char num_row)
{
	char x=0;
	do
	{
		font_printChar(x,num_row*8,*text);
		x+=6;
		text++;
	}while(*text!=0);

}

void font_SetColor(unsigned short font,unsigned short background)
{
	colorFt=font;
	colorBg=background;
}

void font_printDec(unsigned char x,unsigned char y,unsigned char val)
{
	unsigned ret=0;

	ret=val/100+0x30;
	if(ret>0x30)
		{
			font_printChar(x,y,ret);
			x+=6;
			ret=val%100;
		}

	ret=val/10+0x30;
	font_printChar(x,y,ret);
	ret=val%10+0x30;
	font_printChar(x+6,y,ret);

}

